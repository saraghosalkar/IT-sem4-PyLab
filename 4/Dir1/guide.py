import math

def add(a,b):
    return a+b

def sub(a,b):
    return a-b

def mul(a,b):
    return a*b

def div(a,b):
    return a/b

def mod(a,b):
    return a%b

def minimum(a,b):
    return min(a,b)

def maximum(a,b):
    return max(a,b)

def square(a):
    return a**2

def cube(a):
    return a**3

def root(a):
    return math.sqrt(a)
